public class chawon {
    public static void main(String[] argc){
        int[][] arr={{1,11,12,6,5},{10,2,7,4,15},{9,8,3,13,14}};
        for (int i=0;i<arr.length;i++){
            for (int j=0;j<arr[i].length;j++){
                System.out.print(arr[i][j] + " ");
            }
            System.out.println();
        }
    }
}
